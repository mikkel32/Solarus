from collections.abc import Iterable
from pathlib import Path


DATA_PATH = Path("data/intent_dataset.csv")


def parse_dataset(path: Path) -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    with path.open(encoding="utf-8") as handle:
        next(handle, None)  # skip header if present
        for raw_line in handle:
            line = raw_line.rstrip("\n")
            if not line.strip():
                continue
            try:
                row = next(csv.reader([line]))
            except csv.Error:
                # Fall back to a conservative split on the final comma.
                text_chunk, label_chunk = line.rsplit(",", 1)
                text, label = text_chunk.strip(), label_chunk.strip()
            else:
                if len(row) < 2:
                    continue
                label = row[-1].strip()
                text = ",".join(row[:-1]).strip()
            if not text or not label:
                continue
            rows.append({"text": text, "label": label})
    return rows


PATTERNS: tuple[str, ...] = (
    "As a rule of thumb",
    "Please consider to",
    "Have you ever noticed that",
    "It's important that you",
    "Always remember to",
    "Don't forget to",
    "Today's forecast calls for",
    "In my opinion",
    "For best results",
    "Interestingly",
    "Sometimes it's best",
    "Feel free to reach out if you need assistance.",
)


def should_remove(text: str) -> bool:
    for pattern in PATTERNS:
        if pattern in text:
            return True
    if text.startswith("Did you know that") and "?" not in text:
        return True
    return False


TEXT_NORMALIZATIONS = {
    "I would tell you a construction joke,but I'm still working on it.": "I would tell you a construction joke, but I'm still working on it.",
}


def deduplicate(rows: Iterable[dict[str, str]]) -> list[dict[str, str]]:
    seen: set[tuple[str, str]] = set()
    unique_rows: list[dict[str, str]] = []
    for row in rows:
        text = row["text"].strip()
        label = row["label"].strip()
        text = TEXT_NORMALIZATIONS.get(text, text)
        key = (text, label)
        if not text or not label:
            continue
        if key in seen:
            continue
        seen.add(key)
        unique_rows.append({"text": text, "label": label})
    return unique_rows


